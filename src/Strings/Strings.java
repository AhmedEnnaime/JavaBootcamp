package Strings;

public class Strings {
    public  void  strings() {
        String myName = "Ahmed Ennaime";
        // Transforming Strings to UpperCase
        System.out.println(myName.toUpperCase());
        // Getting a portion of the string
        System.out.println(myName.substring(0,5));
        // Checking if it's empty
        System.out.println(myName.isEmpty());
        // Removing spaces
        System.out.println(myName.trim());
    }
}
