package ArthmeticOperators;

public class Operators {
    public  void operators() {
        int numberOne = 6;
        int numberTwo = 10;
        // Addition
        System.out.println(numberTwo + numberOne);
        // Multiplication
        System.out.println(numberOne * numberTwo);
        // Subtraction
        System.out.println(numberOne - numberTwo);
        // Division
        System.out.println(numberOne % numberTwo);

    }

    public  void  comparison() {
        System.out.println(10 > 10);
        System.out.println(10 >= 10);
        System.out.println(10 < 10);
        System.out.println(10 <= 10);
        System.out.println(10 == 10);
        System.out.println(10 != 10);
    }


}
